public class nestedIfelseEx {
    public static void main(String[] args) {
        int a = 9;
        if (a == 10)
        {
            System.out.println("its is true");
        }
        else if (a == 2)
        {
            System.out.println("its true now");
        }
        else if (a == 9)
        {
            System.out.println("this is true");
        }
        else 
        {
            System.out.println("all false");
        }
    }
}
