public class RelationalEx {
    public static void main(String[] args) {
        int a = 22;
        int b = 33;

        System.out.println("'==' operator ex: " + (a==b) );
        System.out.println("'>=' operator ex: " + (a>=b));
        System.out.println("'<=' operator ex: " + (a<=b));
        System.out.println("'!=' operator ex: " + (a!=b));
        System.out.println("'>' operator ex: " + (a>b));
        System.out.println("'<' operator ex: " + (a<b));
        
    }
}
