public class LogicalEx {
    public static void main(String[] args) {
        boolean a = true ;
        boolean b = false;

        System.out.println("'&&' operator ex: " + (a&&b));
        System.out.println("'||' operator ex: " + (a||b));
        System.out.println("'!' operator ex: " + (!b));
        System.out.println("'!' operator ex: " + (!a));
    }
}
